public class Rectangulo {

    double area(double altura,double base){
        return altura*base;
    }
    int area(int altura,int base){
        return altura*base;
    }
    double perimetro(double altura,double base){
        return 2*altura+2*base;
    }
    int perimetro(int altura,int base){
        return 2*altura+2*base;
    }

}